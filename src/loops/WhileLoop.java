package loops;

/**
 * Initialization
 * Condition
 * Increment/Decrement
 * This is an entry Control Loop
 */
public class WhileLoop {
    public static void main(String[] args) {
        int i = 5;
        while(i >= 1) {
            System.out.println("i = " + i);
            i--;
        }
    }
}
